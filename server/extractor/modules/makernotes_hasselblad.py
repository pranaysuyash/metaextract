def get_hasselblad_field_count() -> int:
    """Return total number of Hasselblad fields."""
    total = 0
    for section in [
        HASSLELBLAD_CAMERA_SETTINGS,
        HASSLELBLAD_SHOT_INFO,
        HASSLELBLAD_PICTURE_STYLE,
        HASSLELBLAD_LENS_INFO,
        HASSLELBLAD_CAMERA_INFO,
        HASSLELBLAD_IMAGE_SETTINGS,
        HASSLELBLAD_EXPOSURE_SETTINGS,
        HASSLELBLAD_FOCUS_SETTINGS,
        HASSLELBLAD_COLOR_SETTINGS
    ]:
        total += len(section)
    return total


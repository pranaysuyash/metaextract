def _extract_quality_tags(ds: Any) -> Dict[str, Any]:
    extracted = {}
    all_tags = (
        IMAGE_QUALITY_METRICS | ARTIFACT_DETECTION | DICOM_CONFORMANCE | QUALITY_ASSURANCE
    )
    for tag, name in all_tags.items():
        try:
            if hasattr(ds, str(tag)):
                value = getattr(ds, str(tag), None)
                if value is not None:
                    extracted[name] = value
        except Exception:
            pass
    return extracted


def _is_quality_control_file(file_path: str) -> bool:
    try:
        file_lower = file_path.lower()
        if file_lower.endswith('.dcm'):
            try:
                import pydicom
                ds = pydicom.dcmread(file_path, stop_before_pixels=True)
                return True
            except Exception:
                pass
        return False
    except Exception:
        return False



def get_sigma_field_count() -> int:
    """Return total number of Sigma/Foveon fields."""
    total = 0
    for section in [
        SIGMA_CAMERA_SETTINGS,
        SIGMA_SHOT_INFO,
        SIGMA_PICTURE_STYLE,
        SIGMA_LENS_INFO,
        SIGMA_FOVEON_CAMERA_SETTINGS,
        SIGMA_FOVEON_IMAGE_SETTINGS,
        SIGMA_FOVEON_EXPOSURE_SETTINGS,
        SIGMA_FOVEON_FOCUS_SETTINGS
    ]:
        total += len(section)
    return total

def get_vendor_field_count(vendor: str) -> int:
    """Return field count for specific vendor."""
    if vendor.lower() == "sigma":
        return get_sigma_field_count()
    return 0
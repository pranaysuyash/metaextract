def get_leica_field_count() -> int:
    """Return total number of Leica fields."""
    total = 0
    for section in [
        LEICA_CAMERA_SETTINGS,
        LEICA_SHOT_INFO,
        LEICA_PICTURE_STYLE,
        LEICA_LENS_INFO,
        LEICA_M_CAMERA_SETTINGS,
        LEICA_M_SHOT_INFO,
        LEICA_M_PICTURE_STYLE,
        LEICA_M_LENS_INFO
    ]:
        total += len(section)
    return total

def get_vendor_field_count(vendor: str) -> int:
    """Return field count for specific vendor."""
    if vendor.lower() == "leica":
        return get_leica_field_count()
    return 0